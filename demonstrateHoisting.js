function demonstrateHoisting(){
    console.log("Before declaration")
    console.log("let",);
    console.log("const",)
    console.log("var ",)

   

    let letlet = "I am a let variable"
    const constconst = "I am a const variable"
    var varvar = "I am a var variable"


    console.log("After declaration")
    console.log("let:",letlet)
    console.log("const:",constconst)
    console.log("var: ",varvar)
}

demonstrateHoisting();

